#   P = (2705691079882681090389589001251962954446177367541711474502428610129, 28)

E222Mod = BigInt(2)^222 - 117

E222Gen = Point(
    FieldElement(2705691079882681090389589001251962954446177367541711474502428610129, E222Mod),
    FieldElement(BigInt(28), E222Mod),
    FieldElement(BigInt(160102), E222Mod)
)

E222 = Curve(ElGamal(E222Gen, E222Mod), "E-222")


